class TestSpansQuery:
    def test_basic(self, handler: AgentQueryHandler, ch: _QueryCapture) -> None:
        handler.spans_query(AgentSpansQueryReq(project_id="p1"))

        assert len(ch.calls) == 2

        _assert_sql_structure(
            "SELECT count() FROM spans s WHERE s.project_id = {p:String}",
            ch.calls[0][0],
        )
        _assert_param_values({"p1"}, ch.calls[0][1])

        _assert_sql_structure(
            f"""
            SELECT {_SPANS_LIST_COLS}
            FROM spans s
            WHERE s.project_id = {{p:String}}
            ORDER BY started_at DESC
            LIMIT {{l:UInt64}} OFFSET {{o:UInt64}}
            """,
            ch.calls[1][0],
        )
        _assert_param_values({"p1", 100, 0}, ch.calls[1][1])

    def test_with_filters_time_sort(
        self, handler: AgentQueryHandler, ch: _QueryCapture
    ) -> None:
        handler.spans_query(
            AgentSpansQueryReq(
                project_id="p1",
                filters=AgentSpansQueryFilters(
                    agent_name="bot",
                    custom_filters=[
                        AgentCustomAttrFilter(
                            attr_key="env", operator="eq", value="prod"
                        )
                    ],
                ),
                start="2026-01-01",
                end="2026-02-01",
                sort_by=[AgentSortBy(field="input_tokens", direction="asc")],
            )
        )

        sql = ch.calls[1][0]
        assert "ORDER BY input_tokens asc" in sql
        _assert_param_values(
            {"p1", "2026-01-01", "2026-02-01", "bot", "env", "prod", 100, 0},
            ch.calls[1][1],
        )

    def test_limit_capped(self, handler: AgentQueryHandler, ch: _QueryCapture) -> None:
        handler.spans_query(AgentSpansQueryReq(project_id="p1", limit=99999))
        assert 10000 in ch.calls[1][1].values()


