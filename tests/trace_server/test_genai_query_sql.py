def _fmt(sql: str) -> str:
    """Normalize SQL whitespace via sqlparse."""
    return sqlparse.format(sql, reindent=True).strip()


def _strip_param_names(sql: str) -> str:
    """Replace {genai_N:Type} with {_:Type} so we can compare SQL structure
    without depending on ParamBuilder's auto-generated names.
    """
    return re.sub(r"\{[^}:]+:([^}]+)\}", r"{_:\1}", sql)


def _assert_sql_structure(expected: str, actual: str) -> None:
    """Assert SQL matches structurally (ignoring auto-generated param names)."""
    exp = _strip_param_names(_fmt(expected))
    act = _strip_param_names(_fmt(actual))
    assert exp == act, f"\nExpected:\n{exp}\n\nGot:\n{act}"


def _assert_param_values(expected_values: set, actual_params: dict) -> None:
    """Assert all expected values appear in the params dict."""
    actual_values = set(actual_params.values())
    missing = expected_values - actual_values
    assert not missing, f"Missing param values: {missing}\nGot: {actual_params}"


class _QueryCapture:
    """Mock query_fn that records every call and returns an empty result."""

    def __init__(self) -> None:
        self.calls: list[tuple[str, dict[str, Any]]] = []

    def __call__(self, sql: str, parameters: dict[str, Any]) -> Any:
        self.calls.append((sql, copy.deepcopy(parameters)))
        result = MagicMock()
        result.result_rows = []
        result.column_names = []
        return result


@pytest.fixture
def ch() -> _QueryCapture:
    return _QueryCapture()


@pytest.fixture
def handler(ch: _QueryCapture) -> AgentQueryHandler:
    return AgentQueryHandler(ch)


